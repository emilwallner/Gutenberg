int		ft_count_words(char const *s, char c)
{
	unsigned int	i;
	int				w;
	int				m;

	w = 0;
	i = 0;
	while (s[i] != '\0')
	{
		m = 0;
		while (s[i] != c)
		{
			m = 1;
			if (s[i] == '\0')
				return (w + 1);
			i++;
		}
		if (m == 1)
			w++;
		i++;
	}
	return (w);
}
